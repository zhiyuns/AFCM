class DiceLoss(_WeightedLoss):
    # * 这个loss计算的是和Dice系数相关的loss，计算公式与dice类似，只不过
    # * 用1减去dice来作为loss
    def __init__(self, eps=0.0001):
        super(DiceLoss, self).__init__()
        self.eps = eps
        self.softmax = nn.Softmax(dim=1)

        return

    def forward(self, pred, target):
        pred = self.softmax(pred)
        target = (pred.detach() * 0).scatter_(1, target.unsqueeze(1), 1)

        numerator = 2 * (pred * target).sum(dim=(0, 2, 3)) + self.eps
        denominator = (pred + target).sum(dim=(0, 2, 3)) + self.eps
        loss_per_channel = (1 - numerator / denominator)
        loss = loss_per_channel.sum() / pred.shape[1]

        return loss


class DiceLoss1class(_WeightedLoss):
    def __init__(self):
        super(DiceLoss1class, self).__init__()

    def forward(self, pred, target):
        N = target.size(0)
        smooth = 0.01

        # input_flat = pred.view(N, -1)
        pred_flat = (torch.sigmoid(pred) > 0.5).float().view(N, -1)
        target_flat = target.view(N, -1)
        reverse_pf = 1 - pred_flat
        reverse_tf = 1 - target_flat

        intersection1 = pred_flat * target_flat
        intersection2 = reverse_pf * reverse_tf

        loss = (2 * (intersection1.sum(1) + intersection2.sum(1)) + smooth) / (
                    pred_flat.sum(1) + reverse_pf.sum(1) + target_flat.sum(1) + reverse_tf.sum(1) + smooth)
        loss = 1 - loss.sum() / N

        return loss


class BCEDiceLoss(nn.Module):
    """Linear combination of BCE and Dice losses"""

    def __init__(self, alpha, beta):
        super(BCEDiceLoss, self).__init__()
        self.alpha = alpha
        self.bce = nn.BCEWithLogitsLoss()
        self.beta = beta
        self.dice = DiceLoss1class()

    def forward(self, input, target):
        return self.alpha * self.bce(input, target) + self.beta * self.dice(input, target)

